#For Adding Values
a = 3
b = 4
print(a+b)

# For subtracting Values
a = 7
b = 4
print(a-b)

# For Mutliplication of the Values
a = 4 * 2
print(a)

# For Dividing the Values
a = 4 / 2
print(a) #it gives output as floating values.

# two multiplication symbols to represent exponents
a = 7 ** 2
print(a)

# you can use multiple operations in one expression.
a = 2 + 3*4
print(a)

b = (2 + 3) * 4
print(b)

# Floats 
a = 2.5 + 4.5
print(a)

b = 4.5 * 2.4
print(b)

# But be aware that you can sometimes get an arbitrary number of decimal places in your answer 
a = 0.2 + 0.1
print(a)

b = 3 * 0.1
print(b)

# When you print a number that was defined using underscores, Python prints only the digits
earth_age = 1000_0000_0000
print(earth_age)

# You can assign values to more than one variable using just a single line
x,y,z = 0,1,2
print(x,y,z)

